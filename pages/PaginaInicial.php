            <h1 class="titulo">
                Resumo
            </h1>
            <p class="conteudo">
                The Boys é uma série de televisão norte americana de super-heróis baseada na serie de histórias em
                quadrinhos de mesmo nome de Garth Ennis e Darick Robertson. Desenvolvida por Eric Kripke para a
                plataforma de streaming Prime Video, segue a equipe de justiceiros de mesmo nome que luta contra os
                Supers, indivíduos superpoderosos que abusam de suas habilidades.
                A série estreou em 26 de julho de 2019. Antes da estreia, a Amazon renovou The Boys para uma segunda
                temporada, que teve início em 4 de setembro de 2020. Antes do lançamento da segunda temporada, foi
                anunciado que a série já estava com uma terceira temporada confirmada.
                Foi adquirida pelo SBT e teve a sua primeira temporada exibida entre 5 de setembro e 24 de outubro de
                2020, aos sábados, às 0h30.
            </p>

            <h1 class="titulo">
                Premissa
            </h1>
            <p class="conteudo">
                The Boys se passa em um universo onde indivíduos superpoderosos são reconhecidos como heróis pelo
                público em geral e pertencem à poderosa corporação Vought International, que os comercializa e monetiza.
                Fora de suas personas heroicas, a maioria é arrogante e corrupta. A série se concentra principalmente em
                dois grupos: os Sete, o principal time de super-heróis da Vought International, e os Rapazes / The Boys,
                vigilantes, que procuram manter os heróis corrompidos sob controle.
                Os Rapazes são liderados por Billy Bruto, que despreza todas as pessoas superpoderosas, e os Sete são
                liderados pelo egoísta e instável, Capitão Pátria. Enquanto ocorre um conflito entre os dois grupos, a
                série também segue os novos membros de cada equipe: Hughie Campbell, dos Rapazes, que se junta aos
                vigilantes depois que sua namorada é morta por um dos Sete; e Annie January / Luz-Estrela dos Sete, uma
                heroína jovem e esperançosa forçada a enfrentar a verdade sobre os heróis que admira.
            </p>

